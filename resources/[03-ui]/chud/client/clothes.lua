                        mAccessory = accessorySkin[_accessory .. '_1']
                        mColor = accessorySkin[_accessory .. '_2']
                    end
    
                    local accessorySkin = {}
                    accessorySkin[_accessory .. '_1'] = mAccessory
                    accessorySkin[_accessory .. '_2'] = mColor
                    TriggerEvent('skinchanger:loadClothes', skin, accessorySkin)

                    inCooldown = false
