AddEventHandler("cframework:skinChanged", function(skin)
    local clothesUserHas = {}

    while not chudReady do Citizen.Wait(0) end

    if skin['mask_1'] ~= 0 then
        clothesUserHas["Mask"] = true
    end

    if skin['helmet_1'] ~= -1 then
        clothesUserHas["Helmet"] = true
    end

    if skin['glasses_1'] ~= -1 then
        clothesUserHas["Glasses"] = true
    end

    if skin['ears_1'] ~= -1 then
        clothesUserHas["Ears"] = true
    end

    if skin['torso_1'] ~= (IsMpPed(PlayerPedId()) == "Male" and 252 or 18) then
        clothesUserHas["shirt"] = true
    end

    if skin['pants_1'] ~= (IsMpPed(PlayerPedId()) == "Male" and 61 or 21) then
        clothesUserHas["Pants"] = true
    end

    if skin['shoes_1'] ~= (IsMpPed(PlayerPedId()) == "Male" and 34 or 35) then
        clothesUserHas["Shoes"] = true
    end

    if skin['bproof_1'] ~= 0 then
        clothesUserHas["kevlar"] = true
    end

    if Gloves[IsMpPed(PlayerPedId())][skin['arms']] ~= nil then
        clothesUserHas["gloves"] = true
    end

    if skin['watches_1'] ~= -1 then
        clothesUserHas["watch"] = true
    end

    if skin['bracelets_1'] ~= -1 then
        clothesUserHas["bracelet"] = true
    end

    if skin['chain_1'] ~= 0 then
        clothesUserHas["necklace"] = true
    end

    if skin['bags_1'] ~= 0 then
        clothesUserHas["backpack"] = true
    end

    SendNUIMessage({
        action = "setSkin",
        skin = clothesUserHas
    })
end)